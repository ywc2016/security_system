package edu.buaa.sem.system.model;

public class MailModel {
	String emailModelSubject;
	String emailModelContent;
	String emailTo;

	public String getEmailModelSubject() {
		return emailModelSubject;
	}

	public void setEmailModelSubject(String emailModelSubject) {
		this.emailModelSubject = emailModelSubject;
	}

	public String getEmailModelContent() {
		return emailModelContent;
	}

	public void setEmailModelContent(String emailModelContent) {
		this.emailModelContent = emailModelContent;
	}

	public String getEmailTo() {
		return emailTo;
	}

	public void setEmailTo(String emailTo) {
		this.emailTo = emailTo;
	}

}
